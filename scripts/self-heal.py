def _now():
    return datetime.datetime.now().isoformat(timespec="seconds")


def sync_worklist(agent_findings, apply):
    """Maintain the self-heal worklist (the agent-tier backlog the next session works).

    Upsert one entry per current agent-tier finding (keyed by check name; refresh
    last_seen + occurrences + latest actions), and AUTO-CLEAR entries whose check is
    no longer failing (the heal landed or the drift cleared). Returns the open list.
    Skips the write in dry-run so tests don't mutate state.
    """
    try:
        existing = {e["key"]: e for e in json.load(open(WORKLIST)).get("items", [])}
    except Exception:
        existing = {}
    current_keys = {f["name"] for f in agent_findings}
    now = _now()
    items = []
    for f in agent_findings:
        k = f["name"]
        prev = existing.get(k)
        items.append({
            "key": k,
            "title": f["detail"],
            "tier": AGENT,
            "actions": f.get("actions", []),
            "first_seen": prev["first_seen"] if prev else now,
            "last_seen": now,
            "occurrences": (prev["occurrences"] + 1) if prev else 1,
        })
    # entries not in current findings are resolved → dropped (auto-clear)
    cleared = [k for k in existing if k not in current_keys]
    if apply:
        try:
            os.makedirs(os.path.dirname(WORKLIST), exist_ok=True)
            json.dump({"updatedAt": now, "items": items}, open(WORKLIST, "w"), indent=2)
        except Exception:
            pass
    return items, cleared


def _meter_actions(m):
    """Build worklist action strings from a meter's samples. Samples are
    meter-specific: signoff-compliance emits dicts ({session, tail, ...}); the
    taxonomy / inbox-quality / curator-format meters emit plain strings. Format by
    shape so one string-sample meter can't crash the whole harness (the prior code
    assumed every sample was a signoff dict and died on the first non-signoff ALERT)."""
    acts = []
    for s in m.get("samples", []):
        if isinstance(s, dict):
            sess = s.get("session", "?")
            tail = (s.get("tail", "") or "")[-120:]
            acts.append(f"FP-candidate {sess}: tune gate regex — tail `{tail}`")
        else:
            acts.append(str(s))
    return acts


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--apply", action="store_true", help="perform safe auto-heals")
    ap.add_argument("--dry-run", action="store_true", help="report only (default)")
    ap.add_argument("--json", action="store_true", help="machine-readable JSON only")
    args = ap.parse_args()
    apply = args.apply and not args.dry_run

    heals = [run_check(fn, apply) for fn in HEALS + load_command_checks()]
    recs = _load_gate_log(24)
    meters = [run_check(lambda a, fn=fn: fn(recs), apply) for fn in METERS]

    healed = [h for h in heals if h["status"] == HEALED]
    # Findings that need someone to act = NEEDS_HUMAN heals + ALERT meters.
    findings = [dict(h, kind="heal") for h in heals if h["status"] == NEEDS_HUMAN] \
             + [dict(m, kind="meter", detail=m["detail"], actions=_meter_actions(m))
                for m in meters if m["status"] == ALERT]

    # Tier each finding; partition.
    for f in findings:
        f.setdefault("escalation", DEFAULT_TIER)
    ant_escalations = [f for f in findings if f["escalation"] == ANT]
    agent_findings = [f for f in findings if f["escalation"] == AGENT]

    worklist, cleared = sync_worklist(agent_findings, apply)

    result = {
        "ts": _now(), "repo": REPO, "applied": apply,
        "heals": heals, "meters": meters,
        "ant_escalations": [{"name": f["name"], "detail": f["detail"],
                             "recommendation": f.get("recommendation",
                                 f"Recommend: review {f['name']}. " + "; ".join(f.get("actions", [])))}
                            for f in ant_escalations],
        "worklist_open": len(worklist),
        "worklist_cleared": cleared,
        "counts": {"healed": len(healed), "ant_escalations": len(ant_escalations),
                   "worklist_open": len(worklist), "cleared": len(cleared)},
    }
    print(json.dumps(result, indent=2))
    if not args.json:
        tag = "APPLY" if apply else "DRY-RUN"
        print(f"[self-heal {tag}] healed={len(healed)} ant_escalations={len(ant_escalations)} "
              f"worklist_open={len(worklist)} cleared={len(cleared)}", file=sys.stderr)
    # Exit non-zero ONLY when Ant must decide — the wrapper gates the @Ant ping on this.
    sys.exit(1 if ant_escalations else 0)


if __name__ == "__main__":
    main()
