# ──────────── declarative command-checks (the self-growing registry) ───────────

def _make_command_check(spec):
    """Turn a health-checks.json entry into a check fn. A command-check passes when its
    cmd exits ok_exit; otherwise it's a finding at the spec's tier (default agent)."""
    ok_exit = int(spec.get("ok_exit", 0))
    timeout = int(spec.get("timeout", 60))

    def _check(apply):
        rc, out = sh(["bash", "-lc", spec["cmd"]], timeout=timeout)
        if rc == ok_exit:
            return {"name": spec["name"], "status": OK,
                    "detail": spec.get("ok_detail", f"{spec['name']} ok"), "actions": []}
        ref = f" (regresses {spec['bug_ref']})" if spec.get("bug_ref") else ""
        # Optional declarative self-heal: in apply mode, run heal_cmd and re-check.
        if apply and spec.get("heal_cmd"):
            sh(["bash", "-lc", spec["heal_cmd"]], timeout=timeout)
            rc2, _ = sh(["bash", "-lc", spec["cmd"]], timeout=timeout)
            if rc2 == ok_exit:
                return {"name": spec["name"], "status": HEALED,
                        "detail": spec.get("fail_detail", spec["name"]) + ref + " — auto-healed",
                        "actions": [f"ran heal: {spec['heal_cmd']}"]}
        tail = [l.strip() for l in out.splitlines() if l.strip()][-3:]
        return {"name": spec["name"], "status": NEEDS_HUMAN,
                "detail": spec.get("fail_detail", f"{spec['name']} check failed") + ref,
                "actions": tail + list(spec.get("actions", [])),
                "escalation": spec.get("tier", AGENT)}
    _check.__name__ = "cmd_" + re.sub(r"[^0-9a-zA-Z]+", "_", spec["name"])
    return _check


def load_command_checks():
    try:
        specs = json.load(open(HEALTH_CHECKS_FILE)).get("checks", [])
    except Exception:
        return []
    out = []
    for s in specs:
        if isinstance(s, dict) and s.get("name") and s.get("cmd") and not s.get("disabled"):
            out.append(_make_command_check(s))
    return out


def run_check(fn, apply):
    """Fault-isolate a single check: a check that throws or returns a bad shape becomes a
    NEEDS_HUMAN finding about itself, never killing the whole run (a blind spot is worse
    than a noisy one)."""
    try:
        r = fn(apply)
        if not isinstance(r, dict) or "name" not in r or "status" not in r:
            raise ValueError(f"bad result shape: {r!r}")
        r.setdefault("detail", "")
        r.setdefault("actions", [])
        return r
    except Exception as e:
        return {"name": getattr(fn, "__name__", "unknown-check"), "status": NEEDS_HUMAN,
                "detail": f"CHECK ERRORED: {e}", "escalation": AGENT,
                "actions": ["a broken check is a blind spot — fix the check fn/spec"]}


# ───────────────────────── LAYER B — compliance meters ───────────────────────

def _load_gate_log(hours=24):
    if not os.path.exists(GATE_LOG):
        return []
    cutoff = datetime.datetime.now() - datetime.timedelta(hours=hours)
    out = []
    for line in open(GATE_LOG):
        line = line.strip()
        if not line:
            continue
        try:
            r = json.loads(line)
            ts = datetime.datetime.fromisoformat(r.get("ts", ""))
        except Exception:
            continue
        if ts >= cutoff:
            out.append(r)
    return out


def meter_signoff(recs):
    """Sign-off standard adherence + false-positive surfacing (the FP loop).

    - block_rate: share of stops the gate had to intervene on (trend; high = the
      model still isn't emitting the block inline).
    - fp_candidates: blocks whose closing text already LOOKED like a sign-off the
      regex rejected (header present but fields didn't match, or ≥2 field words in
      the tail). These are exactly the cases that need a regex tweak — surfaced with
      their tails so the fix is a copy-paste, never auto-applied.
    """
    g = [r for r in recs if r.get("session") and r.get("decision")]
    total = len(g)
    blocks = [r for r in g if r["decision"] == "block"]
    # A TRUE false-positive candidate = a block where the FULL sign-off shape was
    # plausibly present but a gate regex rejected it. Two shapes qualify:
    #   1. header matched but fields didn't → field-regex miss candidate;
    #   2. field labels present AND a header-LIKE bold line (`**…** — …`) in the
    #      tail → header-regex miss candidate.
    # Field words ALONE are not enough: a stop ending `- Next: …` with no header at
    # all is genuine non-compliance (the 07fd0e09 class, reviewed 2026-06-11) and
    # belongs in the block_rate trend, not a daily FP ALERT. (A bare header match is
    # likewise too loose: stray bold-with-separator prose like `**Read + Insert**`.)
    fieldword = re.compile(r"\b(Done|Verified|Check|Next|Asks)\b\s*:", re.I)
    # Line-anchored: a real header opens its own line; mid-sentence bolds like
    # "My lean: **build the bridge** — it's…" must not qualify.
    headerlike = re.compile(r"^\s*\*\*[^*\n]+\*\*\s*(?:—|--)\s*\S", re.M)
    # Either way the MISSING half must look present to a looser regex — h=T alone
    # is a stray prose bold (0a111d59: `**Action required…**` in a shield verdict),
    # f-words alone are a fieldless prose mention.
    fp = []
    for b in blocks:
        tail = b.get("tail", "")
        if b.get("header_match") and not b.get("fields_match") and fieldword.search(tail):
            fp.append(b)
        elif b.get("fields_match") and not b.get("header_match") and headerlike.search(tail):
            fp.append(b)
    block_rate = (len(blocks) / total) if total else 0.0
    # ALERT if any false-positive candidate (regex needs tuning) — the headline ask.
    status = ALERT if fp else PASS
    detail = f"{total} stops/24h · {len(blocks)} blocked ({block_rate:.0%}) · {len(fp)} FP-candidate(s)"
    samples = [{"session": b.get("session", "")[:8],
                "header_match": b.get("header_match"), "fields_match": b.get("fields_match"),
                "tail": b.get("tail", "")[-200:]} for b in fp[:5]]
    return {"name": "signoff-compliance", "status": status, "score": round(1 - block_rate, 3),
            "threshold": "0 FP-candidates", "detail": detail, "samples": samples}


def meter_taxonomy(recs):
    """Drift between tasks.json and the canonical taxonomy SoT (docs/process/taxonomy.yaml):
    off-vocab domain/status or a project with no domain mapping. 0 = clean.

    The taxonomy validator + its SoT are an OPTIONAL overlay (the vocab is
    org/personal-specific; a portable consumer may not ship one). A missing file
    must SKIP this meter, not crash the whole self-heal harness — so guard both the
    validator script and the SoT, and fail soft on any load/scan error."""
    import importlib.util
    lint_path = os.path.join(REPO, "scripts", "taxonomy-lint.py")
    sot_path = os.path.join(REPO, "docs/process", "taxonomy.yaml")
    skip = {"name": "taxonomy-drift", "status": PASS, "score": 1,
            "threshold": "0 off-vocab", "detail": "taxonomy SoT/validator absent — skipped", "samples": []}
    if not (os.path.exists(lint_path) and os.path.exists(sot_path)):
        return skip
    try:
        spec = importlib.util.spec_from_file_location("taxonomy_lint", lint_path)
        tx = importlib.util.module_from_spec(spec)
        spec.loader.exec_module(tx)
        findings = tx._scan_tasks()
    except Exception as e:
        return {**skip, "detail": f"taxonomy lint unavailable — skipped ({e})"}
    status = ALERT if findings else PASS
    return {"name": "taxonomy-drift", "status": status, "score": 0 if findings else 1,
            "threshold": "0 off-vocab", "detail": f"{len(findings)} taxonomy drift finding(s) vs canonical vocab",
            "samples": findings[:5]}


def meter_inbox_quality(recs):
    """Thin Ant-decisions (#326-A safety-net): an owner=ant decision-point that is
    open + undecided but LACKS the structured fields #323 needs to render a clean
    one-tap card — decisionOptions, recommendation, or decisionContext. Without
    them it regresses to a crude regex Yes/No card in the inbox. 0 = every genuine
    Ant-decision is properly structured.

    Detective only — flags thin decisions for enrichment, never blocks authoring.
    Fails soft if the task store is absent/unreadable (a portable consumer may not
    ship one), matching meter_taxonomy's optional-overlay contract."""
    tasks_path = os.path.join(REPO, "docs/process/state/tasks.json")
    skip = {"name": "inbox-quality", "status": PASS, "score": 1,
            "threshold": "0 thin Ant-decisions",
            "detail": "task store absent — skipped", "samples": []}
    if not os.path.exists(tasks_path):
        return skip
    try:
        d = json.load(open(tasks_path))
        ts = d.get("tasks", d)
        ts = list(ts.values()) if isinstance(ts, dict) else ts
    except Exception as e:
        return {**skip, "detail": f"task store unreadable — skipped ({e})"}
    TERMINAL = {"done", "cancelled", "archived"}
    thin = []
    for t in ts:
        if t.get("state") in TERMINAL:
            continue
        if (t.get("owner") or "").strip().lower() != "ant":
            continue
        if (t.get("decision") or "").strip():
            continue  # already decided — the point is closed
        ndp = (t.get("nextDecisionPoint") or "").strip()
        is_decision = (bool(ndp) and not re.match(r"none\b", ndp, re.I)) or bool(t.get("decisionOptions"))
        if not is_decision:
            continue
        missing = [f for f in ("decisionOptions", "recommendation", "decisionContext") if not t.get(f)]
        if missing:
            thin.append(f"{t.get('taskId')} missing {','.join(missing)}")
    status = ALERT if thin else PASS
    return {"name": "inbox-quality", "status": status, "score": 0 if thin else 1,
            "threshold": "0 thin Ant-decisions",
            "detail": f"{len(thin)} owner=ant decision(s) lacking structured #323 fields",
            "samples": thin[:5]}


def meter_curator_format(recs):
    """Curator JSON discipline (#333): count 'Curator returned invalid JSON' /
    'no JSON object' lines in the queue-runner log from the last 24h. Lanes
    imitate formats present in reviewed content (the sign-off-block leak found
    2026-06-12: shield/curator answered in block format after reviewing the
    self-heal audit doc, which quotes meter_signoff patterns), and a malformed
    curator verdict silently degrades the quality gate to a retry. #332's
    raw_decode tolerates trailing prose; this catches the cases it can't.
    Fails soft when the log is absent (runner not installed here)."""
    import time as _time
    log_path = os.path.expanduser("~/.openclaw/logs/queue-runner.log")
    skip = {"name": "curator-format", "status": PASS, "score": 1,
            "threshold": "0 parse failures/24h",
            "detail": "queue-runner log absent — skipped", "samples": []}
    if not os.path.exists(log_path):
        return skip
    try:
        if os.path.getmtime(log_path) < _time.time() - 24 * 3600:
            return {**skip, "detail": "queue-runner log stale (>24h) — skipped"}
        bad = []
        with open(log_path, errors="replace") as f:
            for line in f.readlines()[-4000:]:
                if "Curator returned invalid JSON" in line or "Curator returned no JSON object" in line:
                    bad.append(line.strip()[:160])
    except Exception as e:
        return {**skip, "detail": f"queue-runner log unreadable — skipped ({e})"}
    status = ALERT if bad else PASS
    return {"name": "curator-format", "status": status, "score": 0 if bad else 1,
            "threshold": "0 parse failures/24h",
            "detail": f"{len(bad)} curator JSON parse failure(s) in recent runner log",
            "samples": bad[-5:]}


