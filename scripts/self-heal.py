def _invariant_finding(name, codes, label):
    pat = re.compile(r"\bI(" + "|".join(str(c) for c in codes) + r")\b")
    hits = [l for l in _reconcile_lines() if pat.search(l)]
    if not hits:
        return {"name": name, "status": OK, "detail": f"no {label} invariant violations", "actions": []}
    fails = [h for h in hits if h.startswith("FAIL")]
    status = NEEDS_HUMAN  # both FAIL and WARN go to the agent worklist; bar-tripping is ant-tier (none here)
    return {"name": name, "status": status,
            "detail": f"{len(hits)} {label} drift item(s) ({len(fails)} fail)",
            "actions": [h[:160] for h in hits[:5]] + ["reconcile via scripts/agent-work.py + correct-state"]}


