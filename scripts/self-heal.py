def check_stray_git(apply):
    """Nested .git dirs under projects/ cause wrong-dir writes / silent git failures —
    but ONLY when the outer repo doesn't ignore them. A gitignored nested repo (e.g.
    projects/polymarket-bot, a deliberate public/private carveout with its own remotes)
    is intentional and invisible to outer-repo git ops, so it's NOT a stray. Flag only
    nested .git dirs the outer repo does NOT ignore."""
    rc, out = sh(["bash", "-lc", f"find '{REPO}/projects' -name .git -maxdepth 3 2>/dev/null"])
    strays = []
    for line in out.splitlines():
        d = line.strip()
        if not d:
            continue
        rel = os.path.relpath(d, REPO)
        # outer repo ignores it → deliberate separate repo, not a stray
        ig, _ = sh(["bash", "-lc", f"git -C '{REPO}' check-ignore -q '{rel}'"])
        if ig != 0:
            strays.append(rel)
    if not strays:
        return {"name": "stray-git", "status": OK, "detail": "no un-ignored nested .git under projects/", "actions": []}
    return {"name": "stray-git", "status": NEEDS_HUMAN,
            "detail": f"{len(strays)} un-ignored nested .git dir(s)",
            "actions": [f"investigate {r} — gitignore it (if intentional) or register as submodule" for r in strays]}


