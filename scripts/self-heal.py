def heal_autosync(apply):
    """Auto-sync daemon liveness (#101 — daemon found fully UNLOADED, deferred commits
    stranded). Read-only in dry-run; in apply mode delegates the reload to the proven
    ensure-autosync-loaded.sh (which self-alerts on failure)."""
    label = "ant.openclaw-git-sync"
    plist = os.path.join(HOME, "Library", "LaunchAgents", f"{label}.plist")
    rc, out = sh(["bash", "-lc", f"launchctl list 2>/dev/null | grep -q {label}"])
    loaded = (rc == 0)
    if loaded:
        return {"name": "autosync-daemon", "status": OK, "detail": "daemon loaded", "actions": []}
    if not os.path.exists(plist):
        return {"name": "autosync-daemon", "status": OK, "detail": "daemon not installed on this machine", "actions": []}
    if apply:
        r2, o2 = sh(["bash", os.path.join(REPO, "scripts", "ensure-autosync-loaded.sh")])
        if "reload FAILED" in o2 or "RELOAD FAILED" in o2:
            return {"name": "autosync-daemon", "status": NEEDS_HUMAN, "detail": "daemon unloaded; reload FAILED",
                    "actions": [f"reload manually: launchctl load -w {plist}"]}
        return {"name": "autosync-daemon", "status": HEALED, "detail": "daemon was unloaded — reloaded", "actions": ["reloaded ant.openclaw-git-sync"]}
    return {"name": "autosync-daemon", "status": NEEDS_HUMAN, "detail": "daemon UNLOADED (deferred commits will strand)",
            "actions": [f"reload: launchctl load -w {plist}"]}


