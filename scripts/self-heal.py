def _reconcile_lines():
    if "lines" not in _RECON:
        rc, out = sh(["python3", os.path.join(REPO, "scripts", "agent-work.py"), "reconcile"])
        _RECON["lines"] = [l.strip() for l in out.splitlines() if re.match(r"(WARN|FAIL)\s+I\d", l.strip())]
