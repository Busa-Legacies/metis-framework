#!/usr/bin/env python3
"""self-heal.py — daily deterministic self-heal + compliance metering.

Two layers, complementing the LLM `system-audit.sh` (which is semantic + report-only):

  LAYER A — mechanical heal: cheap deterministic plumbing checks. AUTO-FIX only the
    safe, idempotent, reversible class (broken symlinks, non-exec hooks); everything
    judgment-laden (config drift that could regress newer live state, stray .git
    dirs) is REPORTED, never blind-applied (feedback_validate_heal_commands).

  LAYER B — compliance meters: a pluggable registry that *counts adherence* to
    standards that have a measurable signal, and alerts below threshold. This is the
    generalizable answer to "catch stuff like the 2% sign-off gap more broadly":
    a standard isn't trusted because it exists, it's trusted because a meter measures
    it. Adding a meter is one function appended to METERS.

Output: a JSON result to stdout (the .sh wrapper renders markdown + Discord digest).
Portable: derives repo root from this file; no hardcoded per-machine paths.

Run modes:
  --dry-run   report what WOULD heal, change nothing (default for tests)
  --apply     perform the safe auto-heals (default for the scheduled run)
  --json      emit machine-readable JSON only (no human summary on stderr)
"""
import argparse, json, os, re, subprocess, sys, glob, datetime

REPO = os.path.realpath(os.path.join(os.path.dirname(__file__), ".."))
HOME = os.path.expanduser("~")
