# Repo-tracked registry of bug-derived command checks (added via add-healthcheck.py).
# Each fix that's mechanically detectable registers one entry here, so the harness
# grows itself — no python editing, and it syncs to every machine.
HEALTH_CHECKS_FILE = os.path.join(REPO, "docs", "process", "state", "health-checks.json")
# Machine-local runtime state (each machine self-heals its own plumbing) — NOT in the
